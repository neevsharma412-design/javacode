public class Variable {
    public static void main(String[] args) {

        int number = 10;          
        double price = 99.5;      
        char grade = 'A';         
        String name = "Aditya";   

        System.out.println("Number: " + number);
        System.out.println("Price: " + price);
        System.out.println("Grade: " + grade);
        System.out.println("Name: " + name);
    }
}
